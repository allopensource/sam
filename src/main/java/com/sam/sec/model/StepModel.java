package com.sam.sec.model;

public class StepModel {
	private String saga;
	private String step;
	private String apiPath;
	private String capiPath;
	private String apiMethod;
	private String capiMethod;

	public String getSaga() {
		return saga;
	}

	public void setSaga(String saga) {
		this.saga = saga;
	}

	public String getStep() {
		return step;
	}

	public void setStep(String step) {
		this.step = step;
	}

	public String getApiPath() {
		return apiPath;
	}

	public void setApiPath(String apiPath) {
		this.apiPath = apiPath;
	}

	public String getCapiPath() {
		return capiPath;
	}

	public void setCapiPath(String capiPath) {
		this.capiPath = capiPath;
	}

	public String getApiMethod() {
		return apiMethod;
	}

	public void setApiMethod(String apiMethod) {
		this.apiMethod = apiMethod;
	}

	public String getCapiMethod() {
		return capiMethod;
	}

	public void setCapiMethod(String capiMethod) {
		this.capiMethod = capiMethod;
	}

}
